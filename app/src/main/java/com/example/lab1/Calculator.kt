package com.example.lab1

class Calculator {
    fun add(firstOperand: Double, secondOperand: Double): Double  {
        return firstOperand+secondOperand
    }
    fun subtract(firstOperand: Double, secondOperand: Double) : Double{
        return firstOperand-secondOperand
    }
    fun divide(firstOperand: Double, secondOperand: Double) : Double{
        return firstOperand/secondOperand
    }
    fun multiply(firstOperand: Double, secondOperand: Double) : Double{
        return firstOperand*secondOperand
    }
}